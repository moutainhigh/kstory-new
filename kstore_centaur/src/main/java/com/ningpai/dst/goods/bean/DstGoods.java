package com.ningpai.dst.goods.bean;

/**
 * 电商通创建商品
 */
public class DstGoods {
    //商品id
    private String id;
    //商品名称
    private String goodsName;
    //商品编码(可以唯一标识商品)
    private String goodsNum;
    //零售价
    private String price;
    //计量单位
    private String unit;
    //分类
    private String classify;
    //品牌
    private String brand;
    //长
    private String packetLong;
    //宽
    private String packetWide;
    //高
    private String packetHigh;
    //重量
    private String packetWeight;
    //商品描述
    private String goodDescribe;
    //Sku编码
    private String skuCode;
    //Sku名称
    private String skuName;
    //条形码
    private String skuBarcode;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsNum() {
        return goodsNum;
    }

    public void setGoodsNum(String goodsNum) {
        this.goodsNum = goodsNum;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getClassify() {
        return classify;
    }

    public void setClassify(String classify) {
        this.classify = classify;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getPacketLong() {
        return packetLong;
    }

    public void setPacketLong(String packetLong) {
        this.packetLong = packetLong;
    }

    public String getPacketWide() {
        return packetWide;
    }

    public void setPacketWide(String packetWide) {
        this.packetWide = packetWide;
    }

    public String getPacketHigh() {
        return packetHigh;
    }

    public void setPacketHigh(String packetHigh) {
        this.packetHigh = packetHigh;
    }

    public String getPacketWeight() {
        return packetWeight;
    }

    public void setPacketWeight(String packetWeight) {
        this.packetWeight = packetWeight;
    }

    public String getGoodDescribe() {
        return goodDescribe;
    }

    public void setGoodDescribe(String goodDescribe) {
        this.goodDescribe = goodDescribe;
    }

    public String getSkuCode() {
        return skuCode;
    }

    public void setSkuCode(String skuCode) {
        this.skuCode = skuCode;
    }

    public String getSkuName() {
        return skuName;
    }

    public void setSkuName(String skuName) {
        this.skuName = skuName;
    }

    public String getSkuBarcode() {
        return skuBarcode;
    }

    public void setSkuBarcode(String skuBarcode) {
        this.skuBarcode = skuBarcode;
    }
}
